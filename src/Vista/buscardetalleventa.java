void mostrar() {
        List<cliente> lista = catxx.listar();
        model = (DefaultTableModel) Tabla.getModel();
        Object[] data = new Object[5];
        for (int i = 0; i < lista.size(); i++) {
            data[0] = lista.get(i).getId_clien();
            data[1] = lista.get(i).getNom();
            data[2] = lista.get(i).getDirec();
            data[3] = lista.get(i).getTelefono();
            data[4] = lista.get(i).getEmail();
           


            model.addRow(data);
        }
        
        Tabla.setModel(model);
    }
