    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
 Connection cn;
        Conexion conex = new Conexion();
        Statement st;
        ResultSet rs;

        String[] titulos = {"id_clien", "nom_clien", "direcci_clien", "tel_clien", "email_clien"};
        String[] registros = new String[200];
        String sql = "SELECT * FROM cliente WHERE nom_clien LIKE '%" + txtBuscar.getText() + "%' ";
        model = new DefaultTableModel(null, titulos);
        cn = conex.Conectar();
        try {
            st = (Statement) cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("id_clien");
                registros[1] = rs.getString("nom_clien");
                registros[2] = rs.getString("direcci_clien");
                registros[3] = rs.getString("tel_clien");            
                registros[4] = rs.getString("email_clien");             
                
                model.addRow(registros);
            }
            Tabla.setModel(model);
        } catch (SQLException ex) {
            System.out.println("ERROR AL BUSCAR LOS DATOS DE LA CATEGORIA: " + ex.getMessage());
        }    }//GEN-LAST:event_txtBuscarKeyReleased

